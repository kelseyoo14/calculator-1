def add(num1, num2):
    """Returns the sum of the 1st number and 2nd number"""
    return num1 + num2

def subtract(num1, num2):
    """Returns the difference between 1st  and 2nd number"""
    return num1 - num2

def multiply(num1, num2):
    """Returns the product of the 1st and 2nd number"""
    return num1 * num2

def divide(num1, num2):
    """Returns the quotient of 1st divided by the 2nd number"""
    return num1 / num2

def square(num1):
    """Returns the square of the input"""
    return num1 ** 2

def cube(num1):
    """Returns the cube of the input"""
    return num1 ** 3

def power(num1, num2):
    """Returns the 1st number raised to the power of the 2nd"""
    return num1 ** num2
def mod(num1, num2):
    """Returns the modulo of the 1st number divided by second number"""
    return num1 % num2
